package com.elasticpath.rest.sdk.components;

/**
 * Self of a representation.
 */
public class Self {

	private String href;
	private String uri;

	public String getHref() {
		return href;
	}

	public void setHref(final String href) {
		this.href = href;
	}

	public String getUri() {
		return uri;
	}

	public void setUri(final String uri) {
		this.uri = uri;
	}
}
